public class DataTypes {
    public static void main(String[] args) {
        
        int a = 100;
        System.out.println("Integer (int): " + a);

        float pi = 3.14f;  
        System.out.println("Floating Point (float): " + pi);

        double e = 2.71828;
        System.out.println("Double Precision (double): " + e);

        char letter = 'A';
        System.out.println("Character (char): " + letter);

        boolean isJavaFun = true;
        System.out.println("Boolean (boolean): " + isJavaFun);

        String greeting = "Hello, Java!";
        System.out.println("String : " + greeting);

        short mediumNumber = 32000;
        System.out.println("Short (short): " + mediumNumber);

        long largeNumber = 10000000000L; 
        System.out.println("Long (long): " + largeNumber);
    }
}

    

